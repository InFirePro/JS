// Отримання ваги та росту від користувача (в кілограмах та метрах)
let вага = parseFloat(prompt("Введіть вашу вагу в кілограмах:")); // отримуємо вагу від користувача і перетворюємо в число
let ріст = parseFloat(prompt("Введіть ваш ріст в метрах:")); // отримуємо ріст від користувача і перетворюємо в число

// Обчислення BMI
let bmi = вага / (ріст * ріст); // обчислюємо BMI за формулою

// Виведення результату
console.log("Ваш BMI: " + bmi); // виводимо BMI в консоль

// Визначення категорії ваги
if (bmi < 18.5) {
  // перевіряємо, чи BMI менше 18.5
  console.log("Недостатня вага"); // якщо так, виводимо "Недостатня вага"
} else if (bmi >= 18.5 && bmi < 25) {
  // якщо BMI від 18.5 до 24.9
  console.log("Нормальна вага"); // виводимо "Нормальна вага"
} else if (bmi >= 25 && bmi < 30) {
  // якщо BMI від 25 до 29.9
  console.log("Надлишкова вага"); // виводимо "Надлишкова вага"
} else {
  // якщо BMI 30 або більше
  console.log("Ожиріння"); // виводимо "Ожиріння"
}
